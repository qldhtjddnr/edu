package ch03;

public class OperatorExample2 {
    public static void main(String[] args) {
        boolean play = true;
        System.out.println(play);

        play = !play;// !는 not을 의미한다고 생각하자!
        System.out.println(play);

        play = !play;// !는 not을 의미한다고 생각하자!
        System.out.println(play);

        play = !play;// !는 not을 의미한다고 생각하자!
        System.out.println(play);
    }

}
